/**
 * @summary Sum two numbers
 * @function
 * @public
 *
 * @param {Number} x - first number
 * @param {Number} y - second number
 * @returns {Number} the sum of the two numbers
 *
 * @example
 * const result = sum(5, 3)
 * console.log(result)
 * > 8
 */
const sum = (x, y) => x + y

export default sum
